struct zanderC_state {
    int S[8][256];
    int SB[8][256];
    uint64_t K[16];
    uint64_t KB[16];
    uint64_t KP[16];
    uint64_t KQ[16];
    uint64_t last[2];
    uint64_t next[2];
};

struct zCksa_state {
    uint64_t r[16];
    uint64_t o;
};

uint64_t zC_rotl(uint64_t a, int b) {
    return ((a << b) | (a >> (64 - b)));
}

uint64_t zC_rotr(uint64_t a, int b) {
    return ((a >> b) | (a << (64 - b)));
}

void *zC_F(struct zCksa_state *state) {
    int r;
    for (r = 0; r < 10; r++) {
        state->r[0] += state->r[6];
        state->r[1] ^= state->r[15];
        state->r[2] = zC_rotl((state->r[2] ^ state->r[12]), 9);
        state->r[3] += state->r[9];
        state->r[4] ^= state->r[11];
        state->r[5] = zC_rotr((state->r[5] ^ state->r[10]), 6);
        state->r[6] += state->r[13];
        state->r[7] ^= state->r[8];
        state->r[8] = zC_rotl((state->r[8] ^ state->r[3]), 11);
        state->r[9] += state->r[1];
        state->r[10] ^= state->r[4];
        state->r[11] = zC_rotr((state->r[8] ^ state->r[7]), 7);
        state->r[12] += state->r[0];
        state->r[13] ^= state->r[2];
        state->r[14] = zC_rotl((state->r[14] ^ state->r[0]), 3);
        state->r[15] += state->r[5];
    }
    for (r = 0; r < 16; r++) {
        state->o ^= state->r[r];
    }
}

void zCgen_subkeys(struct zanderC_state * state, unsigned char * key, int keylen, unsigned char * iv, int ivlen, int zrounds) {
    struct zCksa_state kstate;
    int c = 0;
    int i;
    memset(state->K, 0, 16*sizeof(uint64_t));
    memset(state->KB, 0, 16*sizeof(uint64_t));
    memset(state->KP, 0, 16*sizeof(uint64_t));
    memset(state->KQ, 0, 16*sizeof(uint64_t));
    memset(&kstate.r, 0, 16*sizeof(uint64_t));
    memset(&kstate.o, 0, sizeof(uint64_t));
    memset(state->last, 0, 2*sizeof(uint64_t));
    memset(state->next, 0, 2*sizeof(uint64_t));

    for (i = 0; i < (keylen / 8); i++) {
        kstate.r[i] = ((uint64_t)key[c] << 56) + ((uint64_t)key[c+1] << 48) + ((uint64_t)key[c+2] << 40) + ((uint64_t)key[c+3] << 32) + ((uint64_t)key[c+4] << 24) + ((uint64_t)key[c+5] << 16) + ((uint64_t)key[c+6] << 8) + (uint64_t)key[c+7];
        c += 8;
    }
    c = 0;
    for (i = 0; i < (ivlen / 8); i++) {
        state->last[i] = 0;
        state->last[i] = ((uint64_t)iv[c] << 56) + ((uint64_t)iv[c+1] << 48) + ((uint64_t)iv[c+2] << 40) + ((uint64_t)iv[c+3] << 32) + ((uint64_t)iv[c+4] << 24) + ((uint64_t)iv[c+5] << 16) + ((uint64_t)iv[c+6] << 8) + (uint64_t)iv[c+7];
	c += 8;
    }
    for (i = 0; i < 256; i++) {
        kstate.r[i & 0x0F] ^= C0[i];
        kstate.r[i & 0x0F] ^= C1[i];
    }
    for (i = 0; i < zrounds; i++) {
        zC_F(&kstate);
        state->K[i] = 0;
	state->K[i] = kstate.o;
    }
    for (i = 0; i < zrounds; i++) {
        state->KB[i] = 0;
        state->KB[i] ^= state->K[i];
    }
    for (i = 0; i < zrounds; i++) {
        zC_F(&kstate);
        state->K[i] = 0;
	state->K[i] = kstate.o;
    }
    for (i = 0; i < 256; i++) {
        state->K[i & 0x0F] ^= C2[i];
        state->KB[i & 0x0F] ^= C3[i];
    }
    for (i = 0; i < zrounds; i++) {
	state->KP[i] = kstate.r[i];
    }
    for (i = 0; i < zrounds; i++) {
        zC_F(&kstate);
	state->KP[i] ^= kstate.o;
    }
    for (i = 0; i < zrounds; i++) {
	state->KQ[i] = kstate.r[i];
    }
    for (i = 0; i < zrounds; i++) {
        zC_F(&kstate);
	state->KQ[i] ^= kstate.o;
    }


}

void zCgen_sbox(struct zanderC_state * state, unsigned char * key, int keylen, unsigned char * crosskey) {
    int i, o, c;
    int s;
    int j = 0;
    int temp;
    int k[256];
    int kb[256];
    for (i = 0; i < 256; i++) {
        kb[i] = 0;
        k[i] = i;
        k[i] ^= key[c];
        kb[i] = k[i];
        c = (c + 1) % keylen;
        
    }
    for (s = 0; s < 8; s++) {
        for (i = 0; i < 256; i++) {
            state->S[s][i] = 0;
            state->S[s][i] = i;
        }
    }
    for (s = 0; s < 8; s++) {
        for (i = 0; i < 256; i++) {
            j = k[j];
            k[j] = (k[c] + k[j]) & 0xff;
            o = (k[k[j]] + k[j]) & 0xff;
            o = ((uint8_t)C2[o] << 48);
            temp = state->S[s][o];
            state->S[s][o] = state->S[s][o];
            state->S[s][o] = temp;
        }
    }
    for (s = 0; s < 8; s++) {
        for (i = 0; i < 256; i++) {
            j = k[j];
            k[j] = (k[c] + k[j]) & 0xff;
            o = (k[k[j]] + k[j]) & 0xff;
            temp = state->S[s][o];
            state->S[s][o] = state->S[s][o];
            state->S[s][o] = temp;
        }
    }
    for (i = 0; i < 256; i++) {
        k[i] ^= kb[i];
        crosskey[i] = k[i];
    }
}

void zCgen_sboxB(struct zanderC_state * state, unsigned char * key, int keylen, unsigned char * crosskey) {
    int i, o, c;
    int s;
    int j = 0;
    int temp;
    int k[256];
    for (i = 0; i < 256; i++) {
        k[i] = i;
        k[i] ^= crosskey[i];
        
    }
    for (s = 0; s < 8; s++) {
        for (i = 0; i < 256; i++) {
            state->SB[s][i] = 0;
            state->SB[s][i] = i;
        }
    }
    for (s = 0; s < 8; s++) {
        for (i = 0; i < 256; i++) {
            j = k[j];
            k[j] = (k[c] + k[j]) & 0xff;
            o = (k[k[j]] + k[j]) & 0xff;
            o = o ^ (uint8_t)C3[o];
            temp = state->SB[s][o];
            state->SB[s][o] = state->SB[s][o];
            state->SB[s][o] = temp;
        }
    }
    for (s = 0; s < 8; s++) {
        for (i = 0; i < 256; i++) {
            j = k[j];
            k[j] = (k[c] + k[j]) & 0xff;
            o = (k[k[j]] + k[j]) & 0xff;
            temp = state->SB[s][o];
            state->SB[s][o] = state->SB[s][o];
            state->SB[s][o] = temp;
        }
    }
}


uint64_t zCF(struct zanderC_state * state, uint64_t xr) {
    int v, x, y, z, a, b, c, d;
    v = (xr & 0xFF00000000000000) >> 56;
    x = (xr & 0x00FF000000000000) >> 48;
    y = (xr & 0x0000FF0000000000) >> 40;
    z = (xr & 0x000000FF00000000) >> 32;
    a = (xr & 0x00000000FF000000) >> 24;
    b = (xr & 0x0000000000FF0000) >> 16;
    c = (xr & 0x000000000000FF00) >> 8;
    d = (xr & 0x00000000000000FF);

    a = a ^ state->S[4][a];
    b = b ^ state->S[5][b];
    c = c ^ state->S[6][c];
    d = d ^ state->S[7][d];

    a = a ^ state->S[7][d] + state->S[6][a];
    b = b ^ state->S[6][c] + state->S[4][d];
    c = c ^ state->S[5][b];
    d = d ^ state->S[4][a];

    a = a ^ y;
    b = b ^ v;
    c = c ^ z;
    d = d ^ x;
    
    v = v ^ state->S[0][v];
    x = x ^ state->S[1][x];
    y = y ^ state->S[2][y];
    z = z ^ state->S[3][z];

    v = v ^ state->S[1][z] + state->S[2][v];
    x = x ^ state->S[2][y];
    y = y ^ state->S[3][x];
    z = z ^ state->S[0][v];
    xr = ((uint64_t)v << 56) + ((uint64_t)x << 48) + ((uint64_t)y << 40) + ((uint64_t)z << 32) + ((uint64_t)a << 24) + ((uint64_t)b << 16) + ((uint64_t)c << 8) + d;
    return xr;
}

uint64_t zCF2(struct zanderC_state * state, uint64_t xr) {
    int v, x, y, z, a, b, c, d;
    v = (xr & 0xFF00000000000000) >> 56;
    x = (xr & 0x00FF000000000000) >> 48;
    y = (xr & 0x0000FF0000000000) >> 40;
    z = (xr & 0x000000FF00000000) >> 32;
    a = (xr & 0x00000000FF000000) >> 24;
    b = (xr & 0x0000000000FF0000) >> 16;
    c = (xr & 0x000000000000FF00) >> 8;
    d = (xr & 0x00000000000000FF);

    a = a ^ state->SB[4][a];
    b = b ^ state->SB[5][b];
    c = c ^ state->SB[6][c];
    d = d ^ state->SB[7][d];

    a = a ^ state->SB[7][d] + state->SB[6][a];
    b = b ^ state->SB[6][c];
    c = c ^ state->SB[5][b];
    d = d ^ state->SB[4][a];

    a = a ^ v;
    b = b ^ x;
    c = c ^ y;
    d = d ^ z;
    
    v = v ^ state->SB[0][v];
    x = x ^ state->SB[1][x];
    y = y ^ state->SB[2][y];
    z = z ^ state->SB[3][z];

    v = v ^ state->SB[1][z] + state->SB[2][v];
    x = x ^ state->SB[2][y];
    y = y ^ state->SB[3][x];
    z = z ^ state->SB[0][v];
    xr = ((uint64_t)v << 56) + ((uint64_t)x << 48) + ((uint64_t)y << 40) + ((uint64_t)z << 32) + ((uint64_t)a << 24) + ((uint64_t)b << 16) + ((uint64_t)c << 8) + d;
    return xr;
}

uint64_t zCblock_encrypt(struct zanderC_state * state, uint64_t *xl, uint64_t *xr) {
    int i;
    uint64_t temp;
    uint64_t Xl;
    uint64_t Xr;

    Xl = *xl;
    Xr = *xr;
    for (i = 0; i < zrounds; i++) {
        Xl = Xl + state->KP[i];
        Xr = Xr ^ state->K[i];
        Xr = Xr + state->KB[i];
        Xl = zC_rotl(Xl, 9);
        Xl = Xl ^ zCF(state, Xr);
        Xl = Xl + zCF2(state, Xr);
        Xr = Xr + state->KQ[i];
        Xl += Xr;
        Xr += Xl;

        temp = Xl;
        Xl = Xr;
        Xr = temp;

    }
    temp = Xl;
    Xl = Xr;
    Xr = temp;

    *xl = Xl;
    *xr = Xr;
}

uint64_t zCblock_decrypt(struct zanderC_state * state, uint64_t *xl, uint64_t *xr) {
    int i;
    uint64_t temp;
    uint64_t Xl;
    uint64_t Xr;

    Xl = *xl;
    Xr = *xr;
    for (i = (zrounds - 1); i != -1; i--) {
        Xr -= Xl;
        Xl -= Xr;
        Xr = Xr - state->KQ[i];
        Xl = Xl - zCF2(state, Xr);
        Xl = Xl ^ zCF(state, Xr);
        Xl = zC_rotr(Xl, 9);
        Xr = Xr - state->KB[i];
        Xr = Xr ^ state->K[i];
        Xl = Xl - state->KP[i];

        temp = Xl;
        Xl = Xr;
        Xr = temp;

    }
    temp = Xl;
    Xl = Xr;
    Xr = temp;
    
    *xl = Xl;
    *xr = Xr;
}

